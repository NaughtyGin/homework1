example = 'Топинамбур'
print(example[0]) # вывод на экран (консоль) первого символа строки
print(example[-1]) # вывод на экран (консоль) последнего символа строки
print(example[5:]) # вывод на экран (консоль) второй половины строки (с нечётным количеством символов)
print(example[::-1]) # вывод на экран (консоль) строки в обратном порядке (наоборот)
print(example[1::2]) # вывод на экран (консоль) каждого второго символа строки